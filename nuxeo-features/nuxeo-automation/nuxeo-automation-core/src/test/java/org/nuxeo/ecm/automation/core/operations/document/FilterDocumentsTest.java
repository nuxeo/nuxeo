package org.nuxeo.ecm.automation.core.operations.document;

import static org.assertj.core.api.Assertions.assertThat;

import java.util.HashMap;
import java.util.Map;

import javax.inject.Inject;

import org.junit.After;
import org.junit.Before;
import org.junit.Test;
import org.junit.runner.RunWith;
import org.nuxeo.ecm.automation.AutomationService;
import org.nuxeo.ecm.automation.OperationContext;
import org.nuxeo.ecm.automation.OperationException;
import org.nuxeo.ecm.core.api.CoreSession;
import org.nuxeo.ecm.core.api.DocumentModelList;
import org.nuxeo.ecm.core.test.CoreFeature;
import org.nuxeo.ecm.core.test.DefaultRepositoryInit;
import org.nuxeo.ecm.core.test.annotations.RepositoryConfig;
import org.nuxeo.runtime.test.runner.Deploy;
import org.nuxeo.runtime.test.runner.Features;
import org.nuxeo.runtime.test.runner.FeaturesRunner;

@RunWith(FeaturesRunner.class)
@Features(CoreFeature.class)
@Deploy("org.nuxeo.ecm.automation.core")
@RepositoryConfig(init = DefaultRepositoryInit.class)
public class FilterDocumentsTest {

    @Inject
    protected CoreSession session;

    @Inject
    protected AutomationService service;

    protected OperationContext ctx;

    @Before
    public void createOperationContext() {
        ctx = new OperationContext(session);
    }

    @After
    public void closeOperationContext() {
        ctx.close();
    }

    @Test
    public void testFilterDocuments() throws OperationException {
        DocumentModelList docs = session.query("Select * from Document");
        assertThat(docs.size()).isNotEqualTo(1);

        ctx.setInput(docs);

        Map<String, Object> params = new HashMap<>();
        params.put("condition", "Document.name == 'test'");
        DocumentModelList filtered = (DocumentModelList) service.run(ctx, FilterDocuments.ID, params);

        assertThat(filtered).hasSize(1);
    }
}
